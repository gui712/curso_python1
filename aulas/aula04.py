# Tipos int e float
# int: números inteiros positivos ou negativos
print(11) # int

# float: números com casas decimais

print(11.1) # float

# type() - função que retorna o tipo de dado
print(type(19)) # int
print(type(10.1)) # float
print(type("Gui"))
print(type(10), type(10.0), type("10"), type(1))