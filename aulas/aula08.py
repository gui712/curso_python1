# Operadores aritmeticos

adicao = 10 + 20
print('Adição:', adicao)

subtracao = 10 - 20
print('Subtração:', subtracao)

multiplicacao = 10 * 20
print('Multiplicação:', multiplicacao)

divisao = 10 / 3
print('Divisão:', divisao)

divisao_inteira = 10 // 3
print('Divisão inteira:', divisao_inteira)

exponenciacao = 2 ** 20
print('Exponenciação:', exponenciacao)

modulo = 25 % 5
print('Módulo:', modulo)